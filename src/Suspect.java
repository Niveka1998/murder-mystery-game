public class Suspect {
    private String name;
    private String alibi;
    private boolean isGuilty;
    private String weapon;

    public Suspect(String name, String alibi, boolean isGuilty, String weapon){
        this.name= name;
        this.alibi = alibi;
        this.isGuilty = isGuilty;
        this.weapon =weapon;
    }
}
